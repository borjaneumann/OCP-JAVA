package _02_ImplementingInterfaces;

public class _06_WhatAboutEnums {
    /*
    A Java class can have at most one public top-level element, a class or interface.
    This public top-level element could also be an enumeration, or enum for short.

    An enum is a specialized type that defines a set of fixed values.
    It is declared with the enum keyword.
    The following demonstrates a simple example of an enum for Color:

    public enum Color {
        RED, YELOW, BLUE, GREEN, ORANGE, PURPLE
    }
    Like classes and interfaces, enums can have more complex
    formations including methods, private constructors, and instance
    variables.
     */
}
