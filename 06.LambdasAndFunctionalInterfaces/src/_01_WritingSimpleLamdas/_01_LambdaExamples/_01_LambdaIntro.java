package _01_WritingSimpleLamdas._01_LambdaExamples;

public class _01_LambdaIntro {
    /*
    Functional programming is a way of writing code more declaratively. You
    specify what you want to do rather than dealing with the state of objects.
    You focus more on expressions than loops.

    Functional programming uses lambda expressions to write code. A lambda
    expression is a block of code that gets passed around. You can think of a
    lambda expression as an unnamed method. It has parameters and a body
    just like full-fledged methods do, but it doesn’t have a name like a real
    method.

    Lambda expressions are often referred to as lambdas for short.
    You might also know them as closures if Java isn’t your first language.
     */
}
